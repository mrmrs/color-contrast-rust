#[derive(Debug, Deserialize)]
struct ApcaCase {
    y_bg: f64,
    y_txt: f64,
    expect: f64,
}

#[derive(Debug, Deserialize)]
struct Fixtures {
    wcag21: Vec<Wcag21Case>,
    michelson: Vec<MichelsonCase>,
    weber: Vec<WeberCase>,
    lstar: Vec<LstarCase>,
    delta_phi: Vec<DeltaPhiCase>,
    apca: Vec<ApcaCase>,
}

fn fixtures() -> Fixtures {
    // This file is intended to be generated by a small Node.js helper in
    // `color.js/tools/contrast-fixtures.mjs`, which calls into Color.js
    // to compute the numeric parameters and expected results.
    let json = include_str!("contrast_fixtures.json");
    serde_json::from_str(json).expect("failed to parse contrast_fixtures.json")
}

fn approx_eq(a: f64, b: f64, eps: f64) -> bool {
    (a - b).abs() <= eps
}

#[test]
fn wcag21_matches_color_js() {
    let fixtures = fixtures();
    for case in fixtures.wcag21 {
        let actual = contrast_wcag21(case.y1, case.y2);
        assert!(
            approx_eq(actual, case.expect, 1e-6),
            "wcag21 mismatch: {:?}, got {actual}",
            case
        );
    }
}

