use color_contrast::{
    contrast_apca_from_luminance, contrast_delta_phi, contrast_lstar, contrast_michelson,
    contrast_wcag21, contrast_weber,
};
use serde::Deserialize;

#[derive(Debug, Deserialize)]
struct Wcag21Case {
    y1: f64,
    y2: f64,
    expect: f64,
}

#[derive(Debug, Deserialize)]
